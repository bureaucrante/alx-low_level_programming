#include <stdio.h>

int main(void)
{
	int i = 0;
	while (i < 10)
	{
		printf("%d\n", i);
		i++;
	}

	return(0);
}

// the while loop is structurally very similar to the for loop, with the exception that the varib;e is declared above the loop and the incrememnt comes after the print command or whatever command is enetered. 
